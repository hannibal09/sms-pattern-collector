export function isWhitelistedSender(sender) {
    if (!sender) return false;

    // Normalize: Remove non-alphanumeric, uppercased
    const normalized = sender.toUpperCase();

    // Check if the sender exactly matches or contains any trusted key
    // Optimization: For very large lists (6000+), iterating includes() might be slow.
    // However, exact match is faster. Most SMS senders are like "AXISBK", "TD-AXISBK".
    // Let's try to match sub-parts.
    
    // Fast path: Exact match
    // We convert array to Set for O(1) lookup if we were doing exact match.
    // But we need partial match (e.g. "BZ-HDFCBK" contains "HDFCBK").
    
    // Given 6000+ keys, .some() with .includes() is 6000 string checks per message.
    // That's acceptable for a client-side tool processing a few thousand SMS.
    
    return TRUSTED_SENDERS.some(trusted => normalized.includes(trusted));
}
